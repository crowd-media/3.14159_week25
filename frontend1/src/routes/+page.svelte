<h2>TL;DR:</h2>
<p>
	“El Debate” is a dialog system that pins two LLM powered agents against each other. There is also
	a referee, who decides who of the two agents wins once the debate ends. These agents are assigned
	a personality and character, and they debate about a topic. All this will be configurable. The
	conversation is presented as a chat between the two agents, in text and audio format.
</p>

